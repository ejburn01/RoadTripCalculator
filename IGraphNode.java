interface IGraphNode {
    /**
     * Returns the name of the node.
     */
    public String getCity();

    /**
     * 
     * Returns the x value.
     * 
     */

    public double getX();

    /**
     * 
     * Returns the y value.
     * 
     */

    public double getY();

    /**
     * 
     * Returns the the name of the state.
     * 
     */

    public String getStateName();

    /**
     * 
     * Returns the population of the state.
     * 
     */

    public int getPopulation();

}
